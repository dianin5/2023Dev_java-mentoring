package com.iKeeper.Day1;

public class Calculate {
    private int n;
    private int m;
    Calculate(){
        this.n=n;
        this.m=m;
    }
    public int Add(){
        return n+m;
    }
    public int Sub() {
        return n-m;
    }
    public int Mul(){
        return n*m;
    }
    public int Div(){
        return n/m;
    }
}